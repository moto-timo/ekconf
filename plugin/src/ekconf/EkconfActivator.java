package ekconf;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator of Ekconf plug-in.
 * @author Tiana Rakotovao
 */
public class EkconfActivator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "ekconf"; //$NON-NLS-1$

	// The shared instance
	private static EkconfActivator plugin;
	
	/**
	 * The constructor
	 */
	public EkconfActivator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static EkconfActivator getDefault() {
		return plugin;
	}

}
